void main() {
  /// operator perbandingan dapat digunakan oleh tipe data string dan int
  /// operator perbandingan ada 6 yaitu :
  int angka1 = 50;
  int angka2 = 100;

  print(angka1 > angka2); // lebih besar
  print(angka1 < angka2); // lebih kecil
  print(angka1 >= angka2); // lebih besar sama dengan
  print(angka1 <= angka2); // lebih kecil sama dengan
  print(angka1 == angka2); // sama dengan
  print(angka1 != angka2); // tidak sama dengan

  /// dengan string
  print("Maulana" == "Yusuf");
}